import turtle

turtle.setup(800,600,200,200)
turtle.pensize(5)
turtle.colormode(255)
turtle.pencolor(0,255,0)
turtle.penup()
turtle.seth(-90)
turtle.fd(100)
turtle.seth(0)
turtle.pendown()
turtle.circle(100)
turtle.penup()
turtle.circle(100,135)
turtle.seth(45)
turtle.pendown()
turtle.fd(75)
turtle.seth(-90)
turtle.fd(50)
turtle.penup()
turtle.seth(90)
turtle.fd(50)
turtle.pendown()
turtle.seth(180)
turtle.fd(50)



turtle.done
